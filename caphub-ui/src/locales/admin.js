function getMessageValue(locale, key) {
  return key.split('.').reduce((current, part) => current?.[part], adminMessages[locale]);
}

export function isSupportedAdminLocale(value) {
  return ADMIN_SUPPORTED_LOCALES.includes(value);
}

export function resolveAdminMessage(locale, key, fallback = key) {
  const selectedLocale = isSupportedAdminLocale(locale) ? locale : DEFAULT_ADMIN_LOCALE;
  const value = getMessageValue(selectedLocale, key) ?? getMessageValue(DEFAULT_ADMIN_LOCALE, key);
  return typeof value === 'string' ? value : fallback;
}
